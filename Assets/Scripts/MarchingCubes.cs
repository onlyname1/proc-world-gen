public class MarchingCubes : MonoBehaviour
{
    public float surfaceLevel = 0;
    public bool drawPlayer = false;
    private int chunkSize;
    private float size;
    private float chunkShift;
    [SerializeField]
    Color[] colors = new Color[] { new Color(), new Color(), new Color(), new Color(), new Color(), new Color(), new Color(), new Color(), new Color()};
    [SerializeField]
    GameObject player; 
    [SerializeField]
    ChunkManager chunkManager;
    List<Vector3> vertices;
    List<Vector3> vertexUVs2;
    Dictionary<Vector3, int> seenVertices;
    List<int> triangles;
    public Dictionary<Vector3Int, GameObject> meshes;
    [SerializeField]
    GameObject chunkObject;


    public void TestMesh()
    {
        int amount = 5;
        for (float i = 0; i < chunkShift * amount; i += chunkShift)
        {
            for (float j = 5 * chunkShift; j > -3 * chunkShift; j -= chunkShift)
            {
                for (float k = 0; k < chunkShift * amount; k += chunkShift)
                {
                    CreateMesh(new Vector3(i, j, k));
                }
            }
        }
    }
    // Start is called before the first frame update
    void Start()
    {
        size = chunkManager.m_size;
        chunkSize = chunkManager.m_chunkSize;
        chunkShift = size * chunkSize;
        meshes = new Dictionary<Vector3Int, GameObject>();

        vertices = new List<Vector3>();
        vertexUVs2 = new List<Vector3>();
        seenVertices = new Dictionary<Vector3, int>();
        triangles = new List<int>();

        if (!drawPlayer)
        {
            TestMesh();
        }
    }

    void CreateMesh(Vector3 coord)
    {
        Cube[,,] chunk = chunkManager.GetChunk(coord);
        Vector3Int chunkNum = chunkManager.WorldToChunk(coord);

        // create gameobject that contains mesh
        Mesh mesh;
        if (meshes.ContainsKey(chunkNum))
        {
            mesh = meshes[chunkNum].GetComponent<MeshFilter>().mesh;
        }
        else
        {
            mesh = new Mesh();
            GameObject newChunk = GameObject.Instantiate(chunkObject);
            meshes.Add(chunkNum, newChunk);
            newChunk.GetComponent<MeshFilter>().mesh = mesh;
        }

        for (int x = 0; x < chunkSize; x++)
        {
            for (int y = 0; y < chunkSize; y++)
            {
               for (int z = 0; z < chunkSize; z++)
                {
                    Cube cube = chunk[x, y, z];
                    int cubeCase = 0;
                    for (int i = 0; i < 8; i++)
                    {
                        // sets the ith bit to 1 if not air
                        if (cube.surfaceValues[i] > surfaceLevel)
                        {
                            cubeCase |= 1 << i;
                        }
                    }

                    List<int> vertexIndices = new List<int>();
                    for (int i = 0; i < 16; i++)
                    {
                        int edge = Table.triangulation[cubeCase, i];
                        if (edge != -1)
                        {
                            int vert1 = Table.edgeVert1[edge];
                            int vert2 = Table.edgeVert2[edge];

                            int bigVert = vert1;
                            int smallVert = vert2;

                            if (cube.surfaceValues[vert1] < cube.surfaceValues[vert2])
                            {
                                bigVert = vert2;
                                smallVert = vert1;
                            }

                            float range = cube.surfaceValues[bigVert] - cube.surfaceValues[smallVert];
                            float val = surfaceLevel - cube.surfaceValues[smallVert];
                            Vector3 vertPos = Vector3.Lerp(cube.vertices[smallVert], cube.vertices[bigVert], val / range);
                            if (seenVertices.ContainsKey(vertPos))
                            {
                                vertexIndices.Add(seenVertices[vertPos]);
                                triangles.Add(seenVertices[vertPos]);
                            }
                            else
                            {
                                vertices.Add(vertPos);
                                Vector4 col4 = colors[(int)cube.materials[bigVert]];
                                vertexUVs2.Add(col4);
                                seenVertices.Add(vertPos, vertices.Count - 1);
                                vertexIndices.Add(vertices.Count - 1);
                                triangles.Add(vertices.Count - 1);
                            }
                        }
                        else
                        {
                            break;
                        }
                    }
                    cube.meshVertices = vertexIndices;
                } 
            }
        }

        UpdateMesh(mesh);
        seenVertices.Clear();
        vertexUVs2.Clear();
        vertices.Clear();
        triangles.Clear();
    }

    void UpdateMesh(Mesh mesh)
    {
        mesh.Clear();

        mesh.SetVertices(vertices);
        mesh.SetUVs(0, vertexUVs2);
        mesh.triangles = triangles.ToArray();
        
        mesh.RecalculateNormals();
    }

